  <h3>Airport Transportation</h3>
  <p>When traveling from the Airport and paying via credit card, you will need to pay in advance via the credit card machine at the Airport Taxi stand. Traveling from other destinations, before entering the taxi, check to ensure they accept credit cards. </p>
  
  <ul>
    <li>JFK INTERNATIONAL AIRPORT</li>
    <li>Taxi Fares: $45.00 flat rate plus any tolls PLUS NY State Tax Surcharge of $.50 per trip to/from Manhattan. Destinations outside of Manhattan are charged via the meter rate. For multiple stops or pickups, the flat rate of $45.00 will be collected at the first stop. Then the taxi meter will be turned on and the last passenger will then pay the final meter rate.</li>
  </ul>

  <ul>
    <li>NEWARK INTERNATIONAL AIRPORT</li>
    <li>Taxi Fares: $50-$75 (aprox.) to any Manhattan location. Additional charges include all tolls, luggage over 24 inches. </li>
  </ul>

  <ul>
    <li>LAGUARDIA AIRPORT</li>
    <li>Taxi Fares: $26-30.00 plus any tolls PLUS NY State Tax Surcharge of $.50 per trip to/from Manhattan. </li>
  </ul>


  <ul>
    <li>AIRPORT SHUTTLE</li>
    <li>Fares: $15-$19 per person, depending on pickup point and destination</li>
    <li>Details: Available 24 hours a day; door to door service. No reservations are required from the airport. Reservations required going to the airport. </li>
  </ul>

